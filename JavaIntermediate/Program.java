package JavaIntermediate;

public class Program {
    // int datatype
    public int max (int a, int b) {
        if (a > b) {
            return a;
        }
        return b;
    }
    // double datatype
    public double max (double a, double b) {
        if (a > b) {
            return a;
        }
        return b;
    }
}
